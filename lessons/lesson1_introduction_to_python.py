# What is Python?
# Python is a high-level, interpreted programming language known for its simplicity.

# Writing your first Python program
print("Hello, World!")  # Output: Hello, World!

# Basic syntax: Variables and Data Types
# Let's declare some variables of different types
name = "Alice"       # String
age = 25             # Integer
height = 5.7         # Float
is_student = True    # Boolean

# Printing multiple variables
print(name, age, height, is_student)  # Output: Alice 25 5.7 True

# Using comments for documentation
# Single-line comment: Use the `#` symbol
# Multi-line comment: Use triple quotes, as shown in this script

# Basic Arithmetic Operations
a = 10
b = 3
print("Addition:", a + b)          # Output: 13
print("Subtraction:", a - b)       # Output: 7
print("Multiplication:", a * b)    # Output: 30
print("Division:", a / b)          # Output: 3.33

# User Input and String Formatting
user_name = input("Enter your name: ")
print(f"Welcome, {user_name}!")    # Example: Welcome, John!

